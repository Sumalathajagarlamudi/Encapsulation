package encapstopic;

public class Encapsulation {

		 private String name ="suma";
		 private int num = 535;
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getNum() {
			return num;
		}
		public void setNum(int num) {
			this.num = num;
		}
	}


